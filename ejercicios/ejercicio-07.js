//Ejercicio 7: Suma de los 10 primeros números

/*Descripción:
Crear un programa que sume los números del 1 al 10 y muestre el resultado.

Requisitos:

Ir sumando los números del 1 al 10.

Al final, mostrar el resultado de la suma. */

for(let i = 1; i <= 10; i++){
    let suma = 0;

    suma += i;
    
    console.log("La suma de los números del 1 al 10 es:", suma)
}